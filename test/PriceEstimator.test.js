var erc20Instance = new web3.eth.Contract(erc20ABI, tokenAddress);
erc20Instance.methods.decimals().call(function(err,decimals){
	if ( !err ) {
		const tokenbits = (new web3.utils.toBN(10)).pow(new web3.utils.toBN(decimals)); 
		const amount = (new web3.utils.toBN(1)).mul(tokenbits);
		console.log("Amount in wei : ", amount.toString());

		var priceEstimator = new web3.eth.Contract(priceEstimatorABI, priceEstimatorContractAddress);

		priceEstimator.methods.getEstimatedETHforERC20(amount.toString(),tokenAddress).call(function (err,balance) {
			if (!err) {
				console.log(balance);
				const requiredEth = balance[0];
				console.log("Estimated ETH: ", web3.utils.fromWei(requiredEth.toString(), 'ether'))
			} else {
				console.log("Error while getting estimated ETH");
			}
		});
	} else {
		console.log("Error while retrieving decimals");
	}
})

